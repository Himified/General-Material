;Program2
;A set of ten BCD readings is stored in the Input Buffer. Convert the numbers into 
;binary and add the numbers
MVI B,0AH;COUNTER
LXI D,3000H ; SOURCE POINTER
LXI H,0000H ; SUM REGISTER
LOOP: CALL BCD2BIN
MOV C,A
MOV A,B
MVI B,00H
DAD B ;ADDING THE BINARY EQUIVIVALENT OF BCD
INX D
MOV B,A
DCR B
JNZ LOOP
SHLD 3010H ; STORING ANSWER 
HLT

BCD2BIN: PUSH B
PUSH H
LDAX D
ANI 0FH ; UNPACKING BCD
MOV B,A
LDAX D
ANI 0F0H
RRC
RRC
RRC
RRC
MOV H,A
MVI C,09H
MUL10: ADD H ; A*10+B
DCR C
JNZ MUL10
ADD B
POP H
POP B
RET